# Install neovim

sudo apt-get install python-dev python-pip python3-dev python3-pip
sudo apt install i3 ranger
sudo apt install xfce4-power-manager conky-all blueman feh xdotool volumeicon-alsa compton dunst
sudo -H pip3 install py3status==1.3 -vvv
sudo apt install fonts-font-awesome fonts-materialdesignicons-webfont

sudo add-apt-repository ppa:neovim-ppa/stable
sudo apt update
sudo apt install neovim
sudo -H pip3 install neovim

sudo apt install tmux tmuxinator
